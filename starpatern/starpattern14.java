class starpattern14{
public static void main(String[] args){
for(int n=0;n<5;n++){
for(int a=5;a>n;a--){
System.out.print(" ");
}
for(int b=0;b<5;b++){
System.out.print(" *");
}
System.out.println();
}
for(int m=0;m<5;m++){
for(int c=0;c<m;c++){
System.out.print(" ");
}
for(int d=0;d<5;d++){
System.out.print(" *");
}
System.out.println();
}
}
}